public class Torch {
    public String nameOfTorch;

    public void Whitelight() {
        System.out.println(nameOfTorch + " torch shines with white light");
    }

    public void Redlight() {
        System.out.println(nameOfTorch + " torch shines with red light");
    }

    public void Flashlight() {
        System.out.println(nameOfTorch + " torch flashing light");
    }

}
